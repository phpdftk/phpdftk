    /**
     * Total data-codeword capacity per version+level (sum of
     * group1.blocks*group1.dataCw + group2.blocks*group2.dataCw).
     */
    public static function dataCodewordsCapacity(int $version, int $eccLevel): int
    {
        [, $g1n, $g1d, $g2n, $g2d] = self::ECC_BLOCKS[$version - 1][$eccLevel];
        return $g1n * $g1d + $g2n * $g2d;
    }

    /**
     * Number of character-count-indicator bits for the given (version
     * group, mode). Mode codes: 1=numeric, 2=alphanumeric, 4=byte.
     */
    public static function charCountBits(int $version, int $mode): int
    {
        $bucket = match (true) {
            $version <= 9 => 0,
            $version <= 26 => 1,
            default => 2,
        };
        return match ($mode) {
            0b0001 => [10, 12, 14][$bucket], // numeric
            0b0010 => [9, 11, 13][$bucket],  // alphanumeric
            0b0100 => [8, 16, 16][$bucket],  // byte
            default => throw new \InvalidArgumentException("Unknown QR mode {$mode}."),
        };
    }
